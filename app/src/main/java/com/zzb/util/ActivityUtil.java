    //点击相册获取相册
    public static void getPhoneimg(Activity activitys) {
        Intent intent = new Intent();
        // 获取本地相册方法一
        intent.setAction(Intent.ACTION_GET_CONTENT);
        intent.setType("image/*");
        activitys.startActivityForResult(intent, CHOOSE_PHONE);
    }

    //调用照相机
    public static void photograph(Activity activitys) {
        Date date = null;
        String str = null;
        //获取当前时间，进一步转化为字符串
        SimpleDateFormat format = new SimpleDateFormat("yyyyMMddHHmmss");
        date = new Date();
        str = format.format(date);
        String fileName = str + ".jpg";
        File outputfile = new File(activitys.getExternalCacheDir(), fileName);
        try {
            if (outputfile.exists()) {
                outputfile.delete();
            }
            outputfile.createNewFile();
        } catch (IOException e) {
            e.printStackTrace();
        }
        if (Build.VERSION.SDK_INT >= 24) {
            uri = FileProvider.getUriForFile(activitys, "com.zzb.sl", outputfile);

        } else {
            uri = Uri.fromFile(outputfile);
        }

        //拍照
        Intent i = new Intent("android.media.action.IMAGE_CAPTURE");
        i.putExtra(MediaStore.EXTRA_OUTPUT, uri);
        //调用系统照相机
        activitys.startActivityForResult(i, TAKE_PHOTO);
    }

    @TargetApi(19)
    public static void handleImageOnKitKat(Intent data, Activity activitys, ImageView mAddImgae) {
        String imagePath = null;
        Uri uri = data.getData();
        Log.d("TAG", "handleImageOnKitKat: uri is " + uri);
        if (DocumentsContract.isDocumentUri(activitys, uri)) {
            // 如果是document类型的Uri，则通过document id处理
            String docId = DocumentsContract.getDocumentId(uri);
            if ("com.android.providers.media.documents".equals(uri.getAuthority())) {
                String id = docId.split(":")[1]; // 解析出数字格式的id
                String selection = MediaStore.Images.Media._ID + "=" + id;
                imagePath = getImagePath(MediaStore.Images.Media.EXTERNAL_CONTENT_URI, selection, activitys);
            } else if ("com.android.providers.downloads.documents".equals(uri.getAuthority())) {
                Uri contentUri = ContentUris.withAppendedId(Uri.parse("content://downloads/public_downloads"), Long.valueOf(docId));
                imagePath = getImagePath(contentUri, null, activitys);
            }
        } else if ("content".equalsIgnoreCase(uri.getScheme())) {
            // 如果是content类型的Uri，则使用普通方式处理
            imagePath = getImagePath(uri, null, activitys);
        } else if ("file".equalsIgnoreCase(uri.getScheme())) {
            // 如果是file类型的Uri，直接获取图片路径即可
            imagePath = uri.getPath();
        }

        // 根据图片路径显示图片
        displayImage(imagePath, mAddImgae);

    }

    //4.4以下的调用这个方法
    public static void handleImageBeforeKitKat(Intent data, ImageView mAddImgae, Activity activitys) {
        Uri uri = data.getData();
        String imagePath = getImagePath(uri, null, activitys);
        // 根据图片路径显示图片
        displayImage(imagePath, mAddImgae);

    }

    private static String getImagePath(Uri uri, String selection, Activity activitys) {
        String path = null;
        //通过uri和selection来获取真实的图片路径
        Cursor cursor = activitys.getContentResolver().query(uri, null, selection, null, null);
        if (cursor != null) {
            if (cursor.moveToFirst()) {
                path = cursor.getString(cursor.getColumnIndex(MediaStore.Images.Media.DATA));
            }
            cursor.close();
        }
        return path;
    }

